    m.match_id,
    m.match_name,
    m.match_short_name,
    m.match_round_name,
    m.match_round_type,
    m.match_round_number,
    m.match_result                                        AS score,
    m.match_status,
    m.kick_off_time,
    p.player_id,
    p.player_name,
    p.player_firstname,
    p.player_lastname,
    p.player_nationality,
    p.player_birth_date,
    p.player_birth_place,
    p.player_birth_country,
    p.player_height,
    p.player_weight,
    p.player_photo,
    p.player_position,
    p.player_detailed_position,
    dpos.position_name,
    dpos.position_short_code,
    dpos.position_group,
    df.formation,
    t.team_name,
    t.team_short_name,
    t.team_code,
    t.team_logo,
    ot.opponent_team_name,
    ot.opponent_team_short_name,
    ot.opponent_team_code,
    ot.opponent_team_logo,
