    ts.team_side,
    ref.referee_common_name                               AS referee_name,
    ref.referee_display_name,
    ref.referee_nationality,
    st.stadium_name,
    st.stadium_city,
    st.stadium_capacity,
    st.stadium_surface,
    st.stadium_latitude,
    st.stadium_longitude,
    dc.coach_name,
